package com.daniel.cursojava.aula34;

public class ConversaoDeUnidadesDeVolume {
	
	public static float litroParaCentimetroCubico(float n) {
		
		return (float) (n*1000);
	}
	
	public static float metroCubicoParaLitro(float n) {
		
		return (float) (n*1000);
	}
	
	public static float metroCubicoParaPeCubico(float n) {
		
		return (float) (n*35.32);
	}
	
	public static float galaoAmericanoParaPolegadaCubica(float n) {
		
		return (float) (n*231);
	}
	
	public static float galaoAmericanoParaLitro(float n) {
		
		return (float) (n*3.785);
	}
}
